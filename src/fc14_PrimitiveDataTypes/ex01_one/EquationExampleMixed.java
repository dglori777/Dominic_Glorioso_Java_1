package fc14_PrimitiveDataTypes.ex01_one;

public class EquationExampleMixed
{
    public static void main(String[] args)
    {
        double assets = 75.75;
        int liabilities = 25;
        double equity = assets - liabilities;
        System.out.println("Assets = Liabilities + Equity");
        System.out.println(assets + " = " + liabilities + " + " + equity);
    }
}
