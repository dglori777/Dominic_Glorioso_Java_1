package s01_basic.ex01_printing;

public class PrintInteger
{
    public static void main(String[] args)
    {
        System.out.printf("Number 10: %d%n", 10);
        System.out.printf("Number 0: %d%n", 0);
        System.out.printf("Number -5: %d%n", -5);
        System.out.printf("Number 1000: %d%n", 1000);
        System.out.printf("Number 42: %d%n", 42);
    }
}
