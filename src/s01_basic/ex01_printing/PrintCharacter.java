package s01_basic.ex01_printing;

public class PrintCharacter
{
    public static void main(String[] args)
    {
        System.out.printf("Letter A: %c%n", 'A');
        System.out.printf("Letter z: %c%n", 'z');
        System.out.printf("Digit 5: %c%n", '5');
        System.out.printf("Symbol #: %c%n", '#');
        System.out.printf("Space: %c%n", ' ');
    }
}
