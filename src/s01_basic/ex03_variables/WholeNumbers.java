package s01_basic.ex03_variables;

public class WholeNumbers
{
    public static void main(String[] args)
    {
        byte smallJar = 127;
        int backpack = 20_000;
        long trunk = 9_876_543_210L;
        System.out.println(smallJar);
        System.out.println(backpack);
        System.out.println(trunk);
    }
}
