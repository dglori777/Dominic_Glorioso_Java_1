package fc25_IfElse;

public class Ex14_TargetProfit
{
    public static void main(String[] args)
    {
        double profit = 50;
        double target = 100;

        if (profit >= target) {
            System.out.println("Time to sell and take a profit!");
        } else {
            System.out.println("Keep holding the option.");
        }
    }
}
